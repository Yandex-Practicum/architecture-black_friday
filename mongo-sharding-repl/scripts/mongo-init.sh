#!/bin/bash

###
# Наполнение БД через mongos
# - БД: somedb
# - Коллекция: helloDoc
# - Кол-во документов: 1000
###

set -euo pipefail

echo "=========================================="
echo "Наполнение базы данных somedb"
echo "=========================================="
echo ""

docker compose exec -T mongos mongosh --port 27017 --quiet <<'EOF'
use somedb

// Вставка 1000 документов
print("Вставка 1000 документов в зашардированную коллекцию helloDoc...\n")

for (var i = 0; i < 1000; i++) {
  db.helloDoc.insertOne({age: i, name: "ly" + i})
}

print("========================================")
print("Наполнение завершено!")
print("========================================\n")
print("Общее количество документов: " + db.helloDoc.countDocuments())
print("\nРаспределение документов по шардам:")
print("========================================\n")

// Показываем распределение по шардам
db.helloDoc.getShardDistribution()
EOF

echo ""
echo "=========================================="
echo "База данных успешно наполнена!"
echo "=========================================="
